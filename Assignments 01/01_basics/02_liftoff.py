def main():
    # Countdown from 10 to 1
    for i in range(10, 0, -1):  # Start at 10, stop before 0, and step down by 1
        print(i, end=" ")  # Print each number on the same line with a space
    
    # Print "Liftoff!" after the countdown
    print("Liftoff!")

if __name__ == "__main__":
    main()
